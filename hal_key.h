#ifndef hal_key_h
#define hal_key_h

#include "hal_key.h"

#define uint8_t unsigned char
#define uint16_t unsigned short
#define uint32_t unsigned int

#define S2_PORT PORTC
#define S2_PIN GPIO_Pin_0

#define S3_PORT PORTC
#define S3_PIN GPIO_Pin_1

#define S4_PORT PORTC
#define S4_PIN GPIO_Pin_2

//按键定义
typedef enum { KEY_S2, KEY_S3, KEY_S4, KEY_NUM } KEY_TYPEDEF;

//按键的状态
typedef enum {
  KEY_IDLE,                 //空闲
  KEY_PRESS,                //按下
  KEY_PRESS_RELEASE,        //按下并释放
  KEY_LONG_PRESS,           //长按
  KEY_LONG_PRESS_CONTINUE,  //长按继续
  KEY_LONG_PRESS_RELEASE,   //长按释放
} KEY_STATUS;

//按键检测过程
typedef enum {
  KEY_STEP_WAIT,            //等待按键按下
  KEY_STEP_PRESS,           //按键按下
  KEY_STEP_LONG_PRESS,      //按键长按
  KEY_STEP_CONTINUE_PRESS,  //按键持续按下
} KEY_STEP_TYPEDEF;

//定义一个函数指针类型
typedef void (*KEY_CALLBACK)(KEY_TYPEDEF key_type, KEY_STATUS key_status);

//按键扫描定时器Tick,每个Tick时间为1ms
#define KEY_SACN_TICK_MS 10  // 10ms

//按键消抖时间,以10ms为Tick
#define KEY_DEBOUNCE_TICK_MS 2  // 20ms

//连续长按时间,以10ms为Tick
#define KEY_LONG_PRESS_TICK_MS 200  // 2s

#endif
